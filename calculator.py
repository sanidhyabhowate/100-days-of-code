# taking first number from user
num1 = int(input("Enter first number: ")) 

# taking second number from user
num2 = int(input("Enter second number: "))

# printing the sum of both numbers
print("Sum =", num1 + num2)

# printing the difference of both numbers
print("Difference =", num1 - num2)

# printing the product of both numbers
print("Product =", num1 * num2)

# printing the quotient of both numbers
print("Quotient =", num1 / num2)